export type ExtRecordKey = `ExtRecord.[${number}]`;

export interface ExtRecordItem {
  // Close, ClosedRecord
  RecordMode: string;
  TimeSection: string[][]; // Двумерный массив строк с временными интервалами
  Redundancy: boolean;
  // Длина записи
  PacketLength: number;
  Mask: string[][]; // Двумерный массив строк с масками
  // Количество секунд до события которые должны быть в записи
  PreRecord: number;
}

export interface ExtRecordRootObject {
  Name: ExtRecordKey;
  Ret: number;
  SessionID: string;
  [key: ExtRecordKey]: ExtRecordItem;
}
